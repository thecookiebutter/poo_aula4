// Funcoes importadas
#include <iostream>
#include <string>
using namespace std;

int main() {
    // Variaveis e seus valores
    int x = 10, y = 20;
    // Condicional comparando variaveis com operadores relacionais
    if(x>y){
        // Mostrando resultado da comparacao ao usuario
        cout << "x é maior que y." << endl;
    }
    // Condicional comparando variaveis com operadores relacionais
    if(x<y){
	// Mostrando resultado da comparacao ao usuario
        cout << "x é menor que y." << endl;
    }
    // Condicional comparando variaveis com operadores relacionais
    if(x>=y){
	// Mostrando resultado da comparacao ao usuario
        cout << "x é maior ou igual a y." << endl;
    }
    // Condicional comparando variaveis com operadores relacionais
    if(x<=y){
	// Mostrando resultado da comparacao ao usuario
        cout << "x é menor ou igual a y." << endl;
    }
    // Condicional comparando variaveis com operadores relacionais
    if(x==y){
	// Mostrando resultado da comparacao ao usuario
        cout << "x é igual a y." << endl;
    }
    // Condicional comparando variaveis com operadores relacionais
    if(x!=y){
	// Mostrando resultado da comparacao ao usuario
        cout << "x é diferente de y." << endl;
    }
    
    return 0;
}