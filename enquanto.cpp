// Funcoes importadas
#include <iostream>
#include <stdlib.h>
using namespace std;

int main() {
    // Variavel
    int num = 1;
    // Roda o codigo enquanto a variavel e menor que dez
    while(num<10){
        //Imprimindo a variavel para o usuario
        cout << "Numero: "<<num<<endl;
        // Aumentando a variavel em um
        num++;
    }
    return 0;
}